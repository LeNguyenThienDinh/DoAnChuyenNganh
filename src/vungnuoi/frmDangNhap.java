                    JOptionPane.showMessageDialog(this, "Xin ch√†o kh√°ch h√†ng!!", "Th√¥ng b√°o",JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    frmKhachHang khachHangFrame = new frmKhachHang();
